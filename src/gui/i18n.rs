//! Internationalization — Polish and English UI strings

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Language {
    English,
    Polish,
}

impl Language {
    pub fn name(&self) -> &'static str {
        match self {
            Language::English => "English",
            Language::Polish => "Polski",
        }
    }
}

